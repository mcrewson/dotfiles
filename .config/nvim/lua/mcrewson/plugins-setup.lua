    "zbirenbaum/copilot.lua",
    cmd = "Copilot",
    event = "InsertEnter",
    config = function()
      require("copilot").setup({
        suggestions = { enabled = false },
        panel = { enabled = false },
      })
    end,
  })
  use({
    "zbirenbaum/copilot-cmp",
    config = function()
      require("copilot_cmp").setup({})
    end,
  })
  use({
    "CopilotC-Nvim/CopilotChat.nvim",
    branch = "canary",
    config = function()
      require("CopilotChat").setup({})
    end,
  })

  if packer_bootstrap then
    require("packer").sync()
  end
end)

